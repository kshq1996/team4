package com.oracle.amuz.model.javabean;

import java.io.Serializable;

public class Address implements Serializable{
	private int address_id;
	private String city;
	private String province;
	private String state;
	private String street;
	private String addressdetail;
	private	int user_id;
	private int phonenum;
	private String name;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAddress_id() {
		return address_id;
	}
	public void setAddress_id(int address_id) {
		this.address_id = address_id;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public String getAddressdetail() {
		return addressdetail;
	}
	public void setAddressdetail(String addressdetail) {
		this.addressdetail = addressdetail;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getPhonenum() {
		return phonenum;
	}
	public void setPhonenum(int phonenum) {
		this.phonenum = phonenum;
	}

	@Override
	public String toString() {
		return "Address [address_id=" + address_id + ", city=" + city + ", province=" + province + ", state=" + state
				+ ", street=" + street + ", addressdetail=" + addressdetail + ", user_id=" + user_id + ", phonenum="
				+ phonenum + ", name=" + name + "]";
	}
	
	public Address(int address_id, String city, String province, String state, String street, String addressdetail,
			int user_id, int phonenum, String name) {
		super();
		this.address_id = address_id;
		this.city = city;
		this.province = province;
		this.state = state;
		this.street = street;
		this.addressdetail = addressdetail;
		this.user_id = user_id;
		this.phonenum = phonenum;
		this.name = name;
	}
	public Address() {
		super();
	}
	
	
}
